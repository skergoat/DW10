<?php 

// while 
$loop = 0; 

while($loop <= 10)
{
    echo "nous revoilà une fois de plus ! <br>";
    $loop++;
}

// concatenation 
$loop = 1; 

while($loop <= 10)
{
    echo "nous revoilà pour la " . $loop. "eme fois ! <br>";
    $loop++;
}

// for 
for($i = 1; $i <= 10 ; $i++)
{
    echo "nous revoilà pour la " . $i. "eme fois ! <br>";
}


